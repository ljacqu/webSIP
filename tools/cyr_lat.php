function asian_title($input){
	$input = explode('_', $input);
	$title = '';

	foreach($input as $item){
		$item = trim($item);
		if(!preg_match('/^[0-9]{3,}$/', $item)){
			$title .= $item.'_';
			continue;
		}
		$title .= '&#'.$item.';';
	}
	return preg_replace('/.*_$/', substr($title, 0, -1), $title);

}


